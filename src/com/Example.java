package com;

public class Example {


        public static void main(String[] args) {

            int count;

            for (count = 0; count < 6; count = count + 1);
            System.out.println("Переменная цикла равна " + count);

            System.out.println("Цикл окончен");

        }  // main(String[]) method

    } // ForStatementDemo class

